package Main.model;

public class Co_so {
	private int maS;
	private String tenCS;
	
	public Co_so() {
		
	}
	
	public Co_so(int maS, String tenCS) {
		super();
		this.maS = maS;
		this.tenCS = tenCS;
	}
	
	public int getMaS() {
		return maS;
	}
	public void setMaS(int maS) {
		this.maS = maS;
	}
	public String getTenCS() {
		return tenCS;
	}
	public void setTenCS(String tenCS) {
		this.tenCS = tenCS;
	}
	
}
